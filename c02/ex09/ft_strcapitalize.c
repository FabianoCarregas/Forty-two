char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (i == 0 && str[i] >= 97 && str[i] <= 122)
		{
			str[i] = str[i] -32;
		}
		else if ((str[i] <= 47) && (str[i +1] >= 97) && (str[i] <= 122))
		{
			str[i +1] = str[i +1] -32;
		}
	i++;
	}
	return (str);
}
